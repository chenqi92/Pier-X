fn render_service(name: &str, svc: &ComposeService, namespace: Option<&str>) -> String {
    let ns_line = match namespace {
        Some(n) => format!("  namespace: {n}\n"),
        None => String::new(),
    };
    let ports = parse_ports(&svc.ports);
    let mut s = String::new();
    s.push_str("apiVersion: v1\n");
    s.push_str("kind: Service\n");
    s.push_str("metadata:\n");
    s.push_str(&ns_line);
    s.push_str(&format!("  name: {name}\n"));
    s.push_str("spec:\n");
    s.push_str("  type: ClusterIP\n");
    s.push_str("  selector:\n");
    s.push_str(&format!("    app: {name}\n"));
    s.push_str("  ports:\n");
    for p in &ports {
        s.push_str(&format!("    - name: tcp-{}\n", p.container));
        s.push_str(&format!("      port: {}\n", p.container));
        s.push_str(&format!("      targetPort: {}\n", p.container));
        s.push_str("      protocol: TCP\n");
    }
    s
}

/// Render a single `networking.k8s.io/v1` Ingress resource that
/// routes the configured host to every HTTP-like service in
/// `routes`. Each route becomes one `paths:` entry under the same
/// host rule:
///
/// ```yaml
/// - host: <ingress.host>
///   http:
///     paths:
///       - path: /<service>
///         pathType: Prefix
///         backend:
///           service:
///             name: <service>
///             port:
///               number: <port>
/// ```
///
/// When `ingress.tls_secret` is set, a `tls:` block matching the
/// same host is emitted. Most clusters also require
/// `ingressClassName` — passed through verbatim when set.
fn render_ingress(
    routes: &[(String, u16)],
    namespace: Option<&str>,
    ingress: &IngressOptions,
) -> String {
    let ns_line = match namespace {
        Some(n) => format!("  namespace: {n}\n"),
        None => String::new(),
    };
    let mut s = String::new();
    s.push_str("apiVersion: networking.k8s.io/v1\n");
    s.push_str("kind: Ingress\n");
    s.push_str("metadata:\n");
    s.push_str(&ns_line);
    s.push_str("  name: pier-x-stack\n");
    s.push_str("spec:\n");
    if !ingress.ingress_class.trim().is_empty() {
        s.push_str(&format!(
            "  ingressClassName: {}\n",
            ingress.ingress_class.trim()
        ));
    }
    if !ingress.tls_secret.trim().is_empty() {
        s.push_str("  tls:\n");
        s.push_str("    - hosts:\n");
        s.push_str(&format!("        - {}\n", ingress.host.trim()));
        s.push_str(&format!(
            "      secretName: {}\n",
            ingress.tls_secret.trim()
        ));
    }
    s.push_str("  rules:\n");
    s.push_str(&format!("    - host: {}\n", ingress.host.trim()));
    s.push_str("      http:\n");
    s.push_str("        paths:\n");
    for (svc, port) in routes {
        s.push_str(&format!("          - path: /{}\n", svc));
        s.push_str("            pathType: Prefix\n");
        s.push_str("            backend:\n");
        s.push_str("              service:\n");
        s.push_str(&format!("                name: {}\n", svc));
        s.push_str("                port:\n");
        s.push_str(&format!("                  number: {}\n", port));
    }
    if routes.len() > 1 {
        s.push_str(
            "      # NOTE: each path-prefix routes traffic to a different service.\n\
             \x20\x20\x20\x20\x20\x20#       Most apps need their root URL configured to match\n\
             \x20\x20\x20\x20\x20\x20#       (e.g. Grafana's GF_SERVER_ROOT_URL) — adjust before\n\
             \x20\x20\x20\x20\x20\x20#       applying.\n",
        );
    }
    s
}

/// Pick the first port from `svc` that smells like an HTTP service.
/// Used to decide whether a service is Ingress-eligible — non-HTTP
/// services (Postgres on 5432, Redis on 6379) get skipped so the
/// generated Ingress only covers things a browser can hit.
fn first_http_port(svc: &ComposeService) -> Option<u16> {
    const HTTP_PORTS: &[u16] = &[
        80, 443, 3000, 4000, 5000, 5601, 8000, 8008, 8080, 8081, 8443, 8888,
        9000, 9090, 9091, 9100,
    ];
    parse_ports(&svc.ports)
        .into_iter()
        .map(|p| p.container)
        .find(|p| HTTP_PORTS.contains(p))
}

