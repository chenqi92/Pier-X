#[derive(Debug, Clone)]
struct ParsedPort {
    /// Container-side port. Host port is intentionally ignored
    /// because Services don't carry one — see module docs.
    container: u16,
}

fn has_published_ports(svc: &ComposeService) -> bool {
    !parse_ports(&svc.ports).is_empty()
}

/// Compose port specs come in three shapes:
///   - short `"5432:5432"` / `"5432"`
///   - long  `{ target: 5432, published: 5432, protocol: tcp }`
///   - just an integer `5432`
fn parse_ports(values: &[serde_yml::Value]) -> Vec<ParsedPort> {
    let mut out = Vec::new();
    for v in values {
        match v {
            serde_yml::Value::String(s) => {
                if let Some(p) = parse_port_string(s) {
                    out.push(p);
                }
            }
            serde_yml::Value::Number(n) => {
                if let Some(c) = n.as_u64().and_then(|n| u16::try_from(n).ok()) {
                    out.push(ParsedPort { container: c });
                }
            }
            serde_yml::Value::Mapping(m) => {
                let target = m
                    .get(serde_yml::Value::String("target".into()))
                    .and_then(|v| v.as_u64())
                    .and_then(|n| u16::try_from(n).ok());
                if let Some(c) = target {
                    out.push(ParsedPort { container: c });
                }
            }
            _ => {}
        }
    }
    out
}

fn parse_port_string(s: &str) -> Option<ParsedPort> {
    // Compose accepts `host:container`, `ip:host:container`,
    // `host:container/proto`, `container/proto`, plain `container`.
    let s = s.trim().trim_matches('"');
    let no_proto = s.split('/').next().unwrap_or(s);
    let parts: Vec<&str> = no_proto.split(':').collect();
    let container_str = parts.last().copied().unwrap_or("");
    container_str
        .trim()
        .parse::<u16>()
        .ok()
        .map(|container| ParsedPort { container })
}

#[derive(Debug, Clone)]
struct NamedMount {
    volume: String,
    mount_path: String,
    read_only: bool,
}

#[derive(Debug, Clone)]
struct BindMount {
    /// Raw Compose source token, including the `./` prefix when
    /// present. Used as a hint in the generated ConfigMap's
    /// placeholder content.
    source: String,
    mount_path: String,
    read_only: bool,
}

#[derive(Debug, Clone, Default)]
struct ParsedVolumes {
    named_mounts: Vec<NamedMount>,
    /// Bind mounts split out from the warnings stream so callers
    /// that opted in to ConfigMap lifting can convert them while
    /// other unsupported volume shapes still surface as warnings.
    bind_mounts: Vec<BindMount>,
    /// Unsupported-volume strings (long-form, malformed, etc.).
    warnings: Vec<String>,
}

fn parse_volumes(values: &[serde_yml::Value]) -> ParsedVolumes {
    let mut out = ParsedVolumes::default();
    for v in values {
        // Long-form: `{ type, source, target, read_only }` — when
        // we can extract the four canonical fields the long form
        // is no harder to translate than the short form. Falls
        // through to the legacy "shaped string" path otherwise.
        if let serde_yml::Value::Mapping(m) = v {
            if let Some(parsed) = parse_long_form_volume(m) {
                push_parsed_long_form(&mut out, parsed);
                continue;
            }
            // Mapping that didn't have the canonical fields —
            // surface it so the user knows we punted instead of
            // silently dropping.
            out.warnings.push("long-form volume (unrecognised shape)".to_string());
            continue;
        }
        let raw = match v {
            serde_yml::Value::String(s) => s.clone(),
            _ => {
                out.warnings.push("non-string volume entry".to_string());
                continue;
            }
        };
        // Strip optional `:ro` / `:rw` suffix.
        let (body, ro) = match raw.rsplit_once(':') {
            Some((head, tail)) if tail == "ro" => (head.to_string(), true),
            Some((head, tail)) if tail == "rw" => (head.to_string(), false),
            _ => (raw.clone(), false),
        };
        // Compose volume short form: `<source>:<target>`. The source
        // can be either a named volume (single token, no slashes) or
        // a host path (contains `/` or `.`).
        let parts: Vec<&str> = body.splitn(2, ':').collect();
        if parts.len() != 2 {
            out.warnings.push(raw);
            continue;
        }
        let source = parts[0].trim();
        let target = parts[1].trim();
        let is_path = source.contains('/')
            || source.starts_with('.')
            || source.starts_with('~');
        if is_path {
            // Anonymise the source path enough to keep ConfigMap
            // generation possible: callers see the verbatim source
            // string and decide whether to lift or warn.
            out.bind_mounts.push(BindMount {
                source: source.to_string(),
                mount_path: target.to_string(),
                read_only: ro,
            });
            // Keep a parallel warning so the legacy "drop with note"
            // path keeps working when `lift_bind_mounts` is off.
            out.warnings.push(raw);
            continue;
        }
        out.named_mounts.push(NamedMount {
            volume: source.to_string(),
            mount_path: target.to_string(),
            read_only: ro,
        });
    }
    out
}

/// Decoded long-form volume — exactly one of the two variants
/// per entry. Kept private to this module; the caller routes
/// each variant into the existing `ParsedVolumes` buckets so the
/// rest of the renderer stays untouched.
enum ParsedLongFormVolume {
    Named { volume: String, mount_path: String, read_only: bool },
    Bind { source: String, mount_path: String, read_only: bool },
    /// Long form with `type:` we don't translate (`tmpfs` /
    /// `npipe` / `cluster`). We surface a warning string the
    /// caller pushes onto `ParsedVolumes.warnings`.
    Unsupported(String),
}

/// Long-form volume parser. Returns `None` if the mapping is
/// missing the canonical `target:` field (without it we can't
/// place anything inside the container and there's nothing useful
/// to do). The `type:` field defaults to `"volume"` per the
/// Compose spec; `read_only:` defaults to false.
fn parse_long_form_volume(m: &serde_yml::Mapping) -> Option<ParsedLongFormVolume> {
    let target = m
        .get(serde_yml::Value::String("target".into()))
        .and_then(|v| v.as_str())
        .map(|s| s.to_string())?;
    let kind = m
        .get(serde_yml::Value::String("type".into()))
        .and_then(|v| v.as_str())
        .unwrap_or("volume")
        .to_string();
    let source = m
        .get(serde_yml::Value::String("source".into()))
        .and_then(|v| v.as_str())
        .map(|s| s.to_string())
        .unwrap_or_default();
    let read_only = m
        .get(serde_yml::Value::String("read_only".into()))
        .and_then(|v| v.as_bool())
        .unwrap_or(false);
    Some(match kind.as_str() {
        "volume" => {
            // `source:` for named volumes is the volume name;
            // when missing the volume is anonymous (we still
            // need a name to mount it, so we synthesise one
            // from the target path so the manifest applies).
            let volume = if source.is_empty() {
                bind_mount_configmap_name("vol", &target)
            } else {
                source
            };
            ParsedLongFormVolume::Named {
                volume,
                mount_path: target,
                read_only,
            }
        }
        "bind" => ParsedLongFormVolume::Bind {
            source: if source.is_empty() {
                ".".to_string()
            } else {
                source
            },
            mount_path: target,
            read_only,
        },
        other => ParsedLongFormVolume::Unsupported(format!(
            "long-form volume type '{other}' not translated"
        )),
    })
}

fn push_parsed_long_form(out: &mut ParsedVolumes, parsed: ParsedLongFormVolume) {
    match parsed {
        ParsedLongFormVolume::Named {
            volume,
            mount_path,
            read_only,
        } => out.named_mounts.push(NamedMount {
            volume,
            mount_path,
            read_only,
        }),
        ParsedLongFormVolume::Bind {
            source,
            mount_path,
            read_only,
        } => {
            out.bind_mounts.push(BindMount {
                source: source.clone(),
                mount_path: mount_path.clone(),
                read_only,
            });
            // Same parallel warning as the short-form bind mount
            // so the legacy `lift_bind_mounts: false` path keeps
            // surfacing the # NOTE about hostPath / ConfigMap.
            out.warnings.push(format!(
                "{}:{}{}",
                source,
                mount_path,
                if read_only { ":ro" } else { "" }
            ));
        }
        ParsedLongFormVolume::Unsupported(msg) => {
            out.warnings.push(msg);
        }
    }
}

fn parse_environment(value: &Option<serde_yml::Value>) -> Vec<(String, String)> {
    let Some(v) = value else { return Vec::new() };
    match v {
        serde_yml::Value::Mapping(m) => m
            .iter()
            .filter_map(|(k, v)| {
                let key = k.as_str()?.to_string();
                let val = match v {
                    serde_yml::Value::String(s) => s.clone(),
                    serde_yml::Value::Number(n) => n.to_string(),
                    serde_yml::Value::Bool(b) => b.to_string(),
                    _ => return None,
                };
                Some((key, val))
            })
            .collect(),
        serde_yml::Value::Sequence(items) => items
            .iter()
            .filter_map(|item| item.as_str())
            .filter_map(|s| {
                let (k, v) = s.split_once('=')?;
                Some((k.trim().to_string(), v.trim().to_string()))
            })
            .collect(),
        _ => Vec::new(),
    }
}

fn command_to_array(value: &serde_yml::Value) -> Vec<String> {
    match value {
        serde_yml::Value::String(s) => {
            // Conservative split — handles the redis template's
            // `redis-server --appendonly yes` style. For anything
            // with quoting / globbing we'd need a real shell parser;
            // none of the built-in templates need that.
            s.split_whitespace().map(|x| x.to_string()).collect()
        }
        serde_yml::Value::Sequence(items) => items
            .iter()
            .filter_map(|i| i.as_str().map(|s| s.to_string()))
            .collect(),
        _ => Vec::new(),
    }
}

/// YAML scalar quoting — wraps the value in double quotes when it
/// would otherwise be parsed as a non-string by a strict YAML
/// reader (numbers, booleans, leading whitespace, special chars).
/// We err on the side of quoting: the output is for humans to apply
/// with `kubectl`, not for golf scoring.
fn yaml_quote(s: &str) -> String {
    let needs_quote = s.is_empty()
        || s.chars().any(|c| matches!(c, ':' | '#' | '"' | '\'' | '\n' | '{' | '}' | '[' | ']' | ',' | '&' | '*' | '|' | '>' | '!' | '%' | '@' | '`'))
        || s.parse::<f64>().is_ok()
        || matches!(s.to_lowercase().as_str(), "true" | "false" | "yes" | "no" | "on" | "off" | "null" | "~");
    if needs_quote {
        format!("\"{}\"", s.replace('\\', "\\\\").replace('"', "\\\""))
    } else {
        s.to_string()
    }
}

#[derive(Debug, Clone, Serialize, Deserialize)]
/// Echo of a converted template — used by the Tauri command return
/// type so the frontend can preview both the source and target side
/// by side without re-fetching.
pub struct ComposeK8sExport {
    /// The original Compose YAML (verbatim from the registry).
    pub compose_yaml: String,
    /// The generated multi-document Kubernetes manifest.
    pub k8s_yaml: String,
    /// Number of Deployments produced.
    pub deployment_count: usize,
    /// Number of Services produced.
    pub service_count: usize,
    /// Number of PersistentVolumeClaims produced.
    pub pvc_count: usize,
    /// Number of Ingress resources produced (0 or 1 — the
    /// converter emits at most one combined Ingress).
    pub ingress_count: usize,
    /// Number of ConfigMap resources produced. Includes both
    /// stubs lifted from bind mounts (when
    /// `IngressOptions.lift_bind_mounts` is on) AND ConfigMaps
    /// emitted from top-level Compose `configs:` declarations.
    pub configmap_count: usize,
    /// Number of `Secret` resources emitted from top-level
    /// Compose `secrets:` declarations.
    pub secret_count: usize,
    /// Number of `NetworkPolicy` resources emitted from
    /// top-level Compose `networks:` declarations.
    pub networkpolicy_count: usize,
    /// Lines flagged for manual attention (bind mounts dropped,
    /// `depends_on` etc).
    pub warnings: Vec<String>,
}

/// Higher-level helper used by the Tauri command. Counts the
/// emitted resources by re-parsing the output (cheap — a few KB)
/// so the dialog can summarise "X Deployments, Y Services, Z PVCs"
/// without the frontend having to count `---` separators itself.
pub fn convert_with_summary(
    compose_yaml: &str,
    namespace: Option<&str>,
) -> Result<ComposeK8sExport, String> {
    convert_with_summary_and_options(
        compose_yaml,
        namespace,
        &IngressOptions::default(),
    )
}

/// Variant of [`convert_with_summary`] that lets the caller pass
/// through Ingress options. Used by the Tauri command layer when
/// the panel's "include Ingress" checkbox is on.
pub fn convert_with_summary_and_options(
    compose_yaml: &str,
    namespace: Option<&str>,
    ingress: &IngressOptions,
) -> Result<ComposeK8sExport, String> {
    let k8s_yaml = convert_with_options(compose_yaml, namespace, ingress)?;
    let mut deployment_count = 0;
    let mut service_count = 0;
    let mut pvc_count = 0;
    let mut ingress_count = 0;
    let mut configmap_count = 0;
    let mut secret_count = 0;
    let mut networkpolicy_count = 0;
    let mut warnings = Vec::new();
    for doc in k8s_yaml.split("\n---\n") {
        let kind = doc
            .lines()
            .find(|l| l.starts_with("kind:"))
            .map(|l| l.trim_start_matches("kind:").trim());
        match kind {
            Some("Deployment") => deployment_count += 1,
            Some("Service") => service_count += 1,
            Some("PersistentVolumeClaim") => pvc_count += 1,
            Some("Ingress") => ingress_count += 1,
            Some("ConfigMap") => configmap_count += 1,
            Some("Secret") => secret_count += 1,
            Some("NetworkPolicy") => networkpolicy_count += 1,
            _ => {}
        }
        for l in doc.lines() {
            let trimmed = l.trim_start();
            if trimmed.starts_with("# NOTE:") {
                warnings.push(trimmed.trim_start_matches("# NOTE:").trim().to_string());
            }
        }
        // ConfigMap / Secret etcd size sanity. The Kubernetes object
        // size cap is 1 MiB *post-encoding* (etcd's request size
        // limit). Our placeholders are tiny, but a user who pastes
        // their actual config blob into the YAML before applying can
        // blow past it. Warn early at 80% so they can split before
        // discovering the failure at `kubectl apply` time.
        if matches!(kind, Some("ConfigMap") | Some("Secret")) {
            check_etcd_size_limit(doc, kind.unwrap_or("?"), &mut warnings);
        }
    }
    Ok(ComposeK8sExport {
        compose_yaml: compose_yaml.to_string(),
        k8s_yaml,
        deployment_count,
        service_count,
        pvc_count,
        ingress_count,
        configmap_count,
        secret_count,
        networkpolicy_count,
        warnings,
    })
}

/// Default soft and hard etcd object-size limits, in bytes. The hard
/// limit (1 MiB) is etcd's per-key request cap that Kubernetes
/// inherits for ConfigMap / Secret bodies. The soft limit (80% of
/// hard) gives users a chance to split their data before they hit
/// the cliff at `kubectl apply` time.
const ETCD_HARD_LIMIT_BYTES: usize = 1024 * 1024;
const ETCD_SOFT_LIMIT_BYTES: usize = 800 * 1024;

/// Read the `metadata.name:` field from a rendered manifest doc.
/// Best-effort only — used purely for human-friendly warning text,
/// so missing / malformed names degrade to "?". The renderer
/// always emits `name:` after `metadata:`, so for our own output
/// this lookup is reliable.
fn extract_metadata_name(doc: &str) -> String {
    let mut in_metadata = false;
    for line in doc.lines() {
        let trimmed = line.trim_start();
        if trimmed == "metadata:" {
            in_metadata = true;
            continue;
        }
        if in_metadata {
            if let Some(rest) = trimmed.strip_prefix("name:") {
                return rest.trim().trim_matches('"').to_string();
            }
            // Out of the metadata block once we see a non-indented
            // top-level key; bail.
            if !line.starts_with(' ') && !line.is_empty() {
                break;
            }
        }
    }
    "?".to_string()
}

/// Push a warning when `doc`'s UTF-8 byte size approaches or exceeds
/// the etcd 1 MiB cap. We measure the rendered YAML — base64 padding
/// for Secrets is already accounted for since the body is what gets
/// stored in etcd verbatim.
fn check_etcd_size_limit(doc: &str, kind: &str, warnings: &mut Vec<String>) {
    let bytes = doc.len();
    if bytes < ETCD_SOFT_LIMIT_BYTES {
        return;
    }
    let name = extract_metadata_name(doc);
    let kib = bytes / 1024;
    if bytes >= ETCD_HARD_LIMIT_BYTES {
        warnings.push(format!(
            "{kind}/{name} is {kib} KiB — exceeds the 1 MiB etcd object-size limit. \
             kubectl apply will fail; split this resource into smaller pieces \
             (e.g. one ConfigMap per directory) or move large blobs into a PVC."
        ));
    } else {
        warnings.push(format!(
            "{kind}/{name} is {kib} KiB — approaching the 1 MiB etcd object-size limit. \
             Consider splitting before `kubectl apply` starts rejecting the resource."
        ));
    }
}

