//! Compose → Kubernetes manifest converter.
//!
//! Targets the small subset of Compose features actually used by
//! Pier-X's built-in templates:
//!
//!   * services with `image` / `command` / `restart` / `environment`
//!     / `ports` / `volumes` / `depends_on`
//!   * top-level named volumes (`volumes:` section) — emitted as
//!     PersistentVolumeClaim manifests
//!
//! For each compose service we emit:
//!
//!   * a `Deployment` (1 replica, recreate strategy by default)
//!   * a `Service` of type `ClusterIP` for any service that exposes
//!     ports (mapped 1:1 from the compose `ports:` host:container
//!     pairs — only the *container* port lands in the Service spec
//!     because Kubernetes services don't carry a "host port" concept;
//!     a NodePort/LoadBalancer would still bind whatever the cluster
//!     decides). Port name = `tcp-<n>`.
//!
//! Bind mounts (`./www:/foo`) are dropped with a comment line — the
//! caller is supposed to translate these to ConfigMaps / hostPath
//! volumes case-by-case. Named volumes survive as a PVC + the same
//! name in the Pod's `volumes:` array.
//!
//! ## What this is NOT
//!
//! Not a full kompose replacement. We deliberately stop at the
//! shape that covers the templates we ship; if a user wants a
//! conversion of a hand-rolled compose with healthchecks / capability
//! drops / sysctls / network specs / secrets, they should run
//! `kompose convert` themselves. The dialog tells them so.

use std::collections::BTreeMap;

use serde::{Deserialize, Serialize};

/// Bag of optional toggles that customise the conversion.
/// Historically named `IngressOptions`; kept as the same struct
/// for migration ease. Contains both Ingress generation knobs and
/// behaviour switches like the bind-mount → ConfigMap lift.
#[derive(Debug, Clone, Default)]
pub struct IngressOptions {
    /// Hostname used for the rule (`Host:` header match). Empty
    /// disables Ingress generation.
    pub host: String,
    /// Optional `ingressClassName` (Kubernetes ≥1.18). Most
    /// clusters need this set explicitly — common values are
    /// `nginx`, `traefik`, `alb`. Empty omits the field.
    pub ingress_class: String,
    /// Optional TLS secret name. When set, a `tls:` block is
    /// emitted referencing this secret for the configured host.
    pub tls_secret: String,
    /// Lift Compose bind mounts (`./local:/in/container[:ro]`)
    /// into Kubernetes `ConfigMap` resources. When true:
    ///
    ///   * One ConfigMap per `(service, source-dir)` pair, named
    ///     `<service>-<sanitised-source>`, with one placeholder
    ///     `README.md` entry that explains how to populate it.
    ///   * The Pod's `volumeMounts:` references the ConfigMap
    ///     instead of being dropped as an unsupported feature.
    ///
    /// Off (the default) keeps the legacy behaviour: bind mounts
    /// are dropped with a `# NOTE:` comment so the user knows to
    /// translate them by hand.
    pub lift_bind_mounts: bool,
}

/// Public entry point. Parses `yaml` as a Compose document and
/// serialises a multi-doc Kubernetes manifest (Deployment +
/// Service + PVC + optional Ingress sections separated by `---`).
///
/// `namespace` is woven into every manifest's metadata when set;
/// callers pass `None` to omit the namespace field (manifests then
/// apply to whatever the user's `kubectl` context defaults to).
///
/// Returns `Err(_)` only when the input fails to parse as YAML — a
/// well-formed-but-empty Compose document yields an empty string,
/// which the frontend renders as "no services to convert".
pub fn convert(yaml: &str, namespace: Option<&str>) -> Result<String, String> {
    convert_with_options(yaml, namespace, &IngressOptions::default())
}

/// Same as [`convert`] but additionally honours the supplied
/// `IngressOptions`. The two-function shape keeps the simple
/// "just convert to K8s" path easy at the call site while still
/// letting the panel pass through ingress configuration.
pub fn convert_with_options(
    yaml: &str,
    namespace: Option<&str>,
    ingress: &IngressOptions,
) -> Result<String, String> {
    let compose: ComposeFile = serde_yml::from_str(yaml)
        .map_err(|e| format!("compose YAML parse failed: {e}"))?;
    let ns = namespace
        .map(|s| s.trim().to_string())
        .filter(|s| !s.is_empty());
    let mut docs: Vec<String> = Vec::new();
    let banner = match &ns {
        Some(n) => format!(
            "# Generated by Pier-X — Compose → Kubernetes (namespace: {n}).\n\
             # Bind mounts and unsupported Compose features are flagged inline.\n\
             # Run `kompose convert` for a higher-fidelity conversion.\n"
        ),
        None => "# Generated by Pier-X — Compose → Kubernetes.\n\
                 # Bind mounts and unsupported Compose features are flagged inline.\n\
                 # Run `kompose convert` for a higher-fidelity conversion.\n"
            .to_string(),
    };
    docs.push(banner);

    // Order services + volumes alphabetically so the output is
    // stable across runs — useful for diffing in version control.
    let mut service_keys: Vec<&String> = compose.services.keys().collect();
    service_keys.sort();
    let mut volume_keys: Vec<&String> = compose.volumes.keys().collect();
    volume_keys.sort();

    for vol in &volume_keys {
        let yaml = render_pvc(vol, ns.as_deref());
        docs.push(yaml);
    }

    // Collect HTTP-eligible (service_name, port) pairs and any
    // ConfigMap stubs lifted from bind mounts as we render each
    // service. ConfigMaps are emitted up-front so a `kubectl apply`
    // creates them before the Pod that references them.
    let mut http_routes: Vec<(String, u16)> = Vec::new();
    let mut configmap_stubs: Vec<ConfigMapStub> = Vec::new();
    let mut deployment_docs: Vec<String> = Vec::new();
    let mut service_docs: Vec<String> = Vec::new();

    for name in &service_keys {
        let svc = &compose.services[*name];
        let (dep_yaml, lifted) =
            render_deployment(name, svc, ns.as_deref(), ingress, &compose)?;
        deployment_docs.push(dep_yaml);
        for stub in lifted {
            configmap_stubs.push(stub);
        }
        if has_published_ports(svc) {
            service_docs.push(render_service(name, svc, ns.as_deref()));
            if let Some(port) = first_http_port(svc) {
                http_routes.push((name.to_string(), port));
            }
        }
    }

    // Top-level secrets / configs become Secret / ConfigMap stubs
    // emitted up-front so kubectl apply orders them before the
    // referencing Pod. Sorted alphabetically for stable diffs.
    let mut secret_keys: Vec<&String> = compose.secrets.keys().collect();
    secret_keys.sort();
    for k in &secret_keys {
        docs.push(render_secret_stub(k, &compose.secrets[*k], ns.as_deref()));
    }
    let mut config_keys: Vec<&String> = compose.configs.keys().collect();
    config_keys.sort();
    for k in &config_keys {
        docs.push(render_top_level_configmap(
            k,
            &compose.configs[*k],
            ns.as_deref(),
        ));
    }

    let mut network_keys: Vec<&String> = compose.networks.keys().collect();
    network_keys.sort();
    for k in &network_keys {
        docs.push(render_network_policy(k, ns.as_deref()));
    }

    for stub in &configmap_stubs {
        docs.push(render_configmap_stub(stub, ns.as_deref()));
    }
    docs.extend(deployment_docs);
    docs.extend(service_docs);

    if !ingress.host.trim().is_empty() && !http_routes.is_empty() {
        docs.push(render_ingress(&http_routes, ns.as_deref(), ingress));
    }

    Ok(docs.join("\n---\n"))
}

/// One ConfigMap to emit for a lifted bind mount. The render layer
/// fills in a placeholder `README.md` entry so the manifest is
/// kubectl-applyable as-is — users are expected to populate the
/// real keys/values via `kubectl create configmap --from-file=`
/// or by hand-editing the manifest before apply.
#[derive(Debug, Clone)]
struct ConfigMapStub {
    /// Final ConfigMap metadata.name.
    name: String,
    /// Original Compose `./<source>` token for the human-readable
    /// hint in the placeholder file.
    source: String,
    /// Compose-side mount path (`/usr/share/nginx/html`) — same
    /// purpose as `source`: a hint, not actually rendered into
    /// the ConfigMap data.
    mount_path: String,
    /// Service that requested this lift. Lets future "apply order"
    /// helpers cross-reference Pod ↔ ConfigMap pairs without
    /// re-parsing the whole manifest.
    #[allow(dead_code)]
    owner_service: String,
}

// ── Compose AST (subset) ────────────────────────────────────────

#[derive(Debug, Default, Deserialize)]
struct ComposeFile {
    #[serde(default)]
    services: BTreeMap<String, ComposeService>,
    #[serde(default)]
    volumes: BTreeMap<String, serde_yml::Value>,
    /// Top-level `secrets:` block. Each value is either a
    /// `{ file: path }` / `{ external: true }` / `{ environment: VAR }`
    /// shape; we honour `file:` (emits a Secret stub) and warn on
    /// the others.
    #[serde(default)]
    secrets: BTreeMap<String, serde_yml::Value>,
    /// Top-level `configs:` block. Same shape grammar as secrets;
    /// emits a ConfigMap stub for `file:` form, warns on others.
    #[serde(default)]
    configs: BTreeMap<String, serde_yml::Value>,
    /// Top-level `networks:` block. Each entry becomes a placeholder
    /// `NetworkPolicy` that allows traffic between pods labelled
    /// with the same network name — the closest analogue to
    /// Compose's "this network groups N services that can talk to
    /// each other".
    #[serde(default)]
    networks: BTreeMap<String, serde_yml::Value>,
}

#[derive(Debug, Default, Deserialize)]
struct ComposeService {
    #[serde(default)]
    image: String,
    #[serde(default)]
    command: Option<serde_yml::Value>,
    #[serde(default)]
    restart: Option<String>,
    #[serde(default)]
    environment: Option<serde_yml::Value>,
    #[serde(default)]
    ports: Vec<serde_yml::Value>,
    #[serde(default)]
    volumes: Vec<serde_yml::Value>,
    #[serde(default)]
    depends_on: Option<serde_yml::Value>,
    /// Service-level `secrets:` references. Each entry is either
    /// a bare name (`- db_password`) or the long form with
    /// `source` / `target` / `mode`. Resolved against the
    /// top-level `secrets:` block.
    #[serde(default)]
    secrets: Vec<serde_yml::Value>,
    /// Service-level `configs:` references — same grammar as
    /// `secrets:`, resolved against top-level `configs:`.
    #[serde(default)]
    configs: Vec<serde_yml::Value>,
    /// Service-level `networks:` membership. Either an array of
    /// names (`- backend`) or a mapping (`backend: {}`). We
    /// ignore long-form details — only the membership names are
    /// used to label the Pod for NetworkPolicy selection.
    #[serde(default)]
    networks: Option<serde_yml::Value>,
    /// Compose `deploy:` block — only `replicas:` is honoured.
    /// Other fields (resources, restart_policy, placement, …)
    /// either don't translate cleanly or already have direct
    /// equivalents elsewhere in the manifest.
    #[serde(default)]
    deploy: Option<ComposeDeploy>,
    /// Service-level `healthcheck:` block. Maps to a Kubernetes
    /// liveness probe via `render_liveness_probe`.
    #[serde(default)]
    healthcheck: Option<ComposeHealthcheck>,
}

#[derive(Debug, Default, Deserialize)]
#[serde(rename_all = "snake_case")]
struct ComposeDeploy {
    /// Number of replicas to run. Maps directly to
    /// `Deployment.spec.replicas`. `0` is treated as "default
    /// (1)" so a hand-edited `replicas: 0` doesn't accidentally
    /// produce an unscheduled stack.
    #[serde(default)]
    replicas: Option<u32>,
    /// Compose `deploy.restart_policy.condition` mirrors the
    /// service-level `restart:` field but lives under `deploy:`.
    /// We honour both, with `deploy.restart_policy.condition`
    /// winning when set (Compose's documented precedence).
    #[serde(default)]
    restart_policy: Option<ComposeRestartPolicy>,
    /// Compose `deploy.resources.{limits,reservations}` — the
    /// closest analog to K8s `resources.{limits,requests}`. We
    /// translate `limits` → `resources.limits`, `reservations`
    /// → `resources.requests`. Compose units (`cpus: '0.5'`,
    /// `memory: '256M'`) are passed through verbatim — both
    /// platforms accept the same string forms.
    #[serde(default)]
    resources: Option<ComposeResources>,
    /// Compose `healthcheck` block — translated to a K8s
    /// `livenessProbe`. The shape mirrors the Compose spec:
    ///
    /// ```yaml
    /// healthcheck:
    ///   test: ["CMD", "curl", "-f", "http://localhost:80"]
    ///   interval: 30s
    ///   timeout: 5s
    ///   retries: 3
    ///   start_period: 5s
    /// ```
    ///
    /// `test:` is the only required field for translation; the
    /// rest map directly onto K8s probe knobs (interval →
    /// `periodSeconds`, retries → `failureThreshold`, etc.).
    #[serde(default)]
    healthcheck: Option<ComposeHealthcheck>,
}

#[derive(Debug, Default, Deserialize)]
#[serde(rename_all = "snake_case")]
struct ComposeResources {
    /// Hard limits (Compose's `deploy.resources.limits`). Maps
    /// to K8s `resources.limits`.
    #[serde(default)]
    limits: Option<ComposeResourceBucket>,
    /// Soft reservations (Compose's `deploy.resources.reservations`).
    /// Maps to K8s `resources.requests`.
    #[serde(default)]
    reservations: Option<ComposeResourceBucket>,
}

#[derive(Debug, Default, Deserialize)]
#[serde(rename_all = "snake_case")]
struct ComposeResourceBucket {
    /// CPU as a fractional core count (`'0.5'`, `'1.0'`).
    /// Stringified so YAML preserves the leading zero / decimal.
    #[serde(default)]
    cpus: Option<serde_yml::Value>,
    /// Memory string (`'256M'`, `'1G'`, `'512Mi'`). Compose and
    /// K8s share the same suffix grammar; we copy verbatim.
    #[serde(default)]
    memory: Option<String>,
}

#[derive(Debug, Default, Deserialize)]
#[serde(rename_all = "snake_case")]
struct ComposeRestartPolicy {
    /// `none` / `on-failure` / `any`. Maps onto the same K8s
    /// `restartPolicy` enum the service-level `restart:` field
    /// already targets:
    ///   * `none` → `Never`
    ///   * `on-failure` → `OnFailure`
    ///   * `any` (default) → `Always` (omitted, K8s default)
    #[serde(default)]
    condition: Option<String>,
}

#[derive(Debug, Default, Deserialize)]
#[serde(rename_all = "snake_case")]
struct ComposeHealthcheck {
    /// Either a `["CMD", argv...]` / `["CMD-SHELL", script]` array,
    /// the literal `["NONE"]`, or a bare string treated as
    /// `["CMD-SHELL", string]`.
    #[serde(default)]
    test: Option<serde_yml::Value>,
    /// Compose duration string ("30s", "1m30s"). Maps to
    /// `livenessProbe.periodSeconds` after parsing.
    #[serde(default)]
    interval: Option<String>,
    /// Per-attempt timeout, e.g. "5s". Maps to `timeoutSeconds`.
    #[serde(default)]
    timeout: Option<String>,
    /// Consecutive failures before declaring unhealthy. Maps to
    /// `failureThreshold`.
    #[serde(default)]
    retries: Option<u32>,
    /// Grace period at startup. Maps to `initialDelaySeconds`.
    #[serde(default)]
    start_period: Option<String>,
    /// Compose-only escape hatch. When `true` the healthcheck is
    /// considered disabled, no probe is emitted.
    #[serde(default)]
    disable: Option<bool>,
}

// ── Renderers ───────────────────────────────────────────────────

fn render_pvc(name: &str, namespace: Option<&str>) -> String {
    let ns_line = match namespace {
        Some(n) => format!("  namespace: {n}\n"),
        None => String::new(),
    };
    format!(
        "apiVersion: v1\n\
         kind: PersistentVolumeClaim\n\
         metadata:\n\
         {ns}\
         \x20\x20name: {name}\n\
         spec:\n\
         \x20\x20accessModes:\n\
         \x20\x20\x20\x20- ReadWriteOnce\n\
         \x20\x20resources:\n\
         \x20\x20\x20\x20requests:\n\
         \x20\x20\x20\x20\x20\x20storage: 1Gi\n",
        ns = ns_line,
        name = name,
    )
}

fn render_deployment(
    name: &str,
    svc: &ComposeService,
    namespace: Option<&str>,
    opts: &IngressOptions,
    compose: &ComposeFile,
) -> Result<(String, Vec<ConfigMapStub>), String> {
    if svc.image.trim().is_empty() {
        return Err(format!(
            "service '{name}' has no `image:` — only image-backed services are supported"
        ));
    }

    let ns_line = match namespace {
        Some(n) => format!("  namespace: {n}\n"),
        None => String::new(),
    };

    let mut s = String::new();
    // Service-level network memberships → Pod labels. The
    // top-level networks block emits a NetworkPolicy per entry
    // that selects on these same labels, so adding them here
    // wires the service into the right "VLAN" without the user
    // having to touch the manifest. Filtered to only networks
    // declared at the top level so a typo doesn't silently
    // bind to nothing.
    let net_memberships: Vec<String> =
        parse_service_networks(&svc.networks, &compose.networks);

    s.push_str("apiVersion: apps/v1\n");
    s.push_str("kind: Deployment\n");
    s.push_str("metadata:\n");
    s.push_str(&ns_line);
    s.push_str(&format!("  name: {name}\n"));
    s.push_str("  labels:\n");
    s.push_str(&format!("    app: {name}\n"));
    s.push_str("spec:\n");
    let replicas = svc
        .deploy
        .as_ref()
        .and_then(|d| d.replicas)
        .filter(|n| *n > 0)
        .unwrap_or(1);
    s.push_str(&format!("  replicas: {replicas}\n"));
    s.push_str("  strategy:\n");
    s.push_str("    type: Recreate\n");
    s.push_str("  selector:\n");
    s.push_str("    matchLabels:\n");
    s.push_str(&format!("      app: {name}\n"));
    s.push_str("  template:\n");
    s.push_str("    metadata:\n");
    s.push_str("      labels:\n");
    s.push_str(&format!("        app: {name}\n"));
    for net in &net_memberships {
        s.push_str(&format!(
            "        pier-x.network/{net}: \"in\"\n"
        ));
    }
    s.push_str("    spec:\n");

    // restartPolicy mapping. Compose has TWO sources for this:
    //   * `restart:` at the service level (legacy; values:
    //     `no` / `always` / `on-failure` / `unless-stopped`)
    //   * `deploy.restart_policy.condition:` (newer Swarm-mode
    //     spec; values: `none` / `on-failure` / `any`)
    // Per the Compose spec `deploy.restart_policy` wins when
    // both are set. Mapping → K8s:
    //   * `no` / `none` / `false` → Never
    //   * `on-failure` → OnFailure
    //   * everything else → omit (Always is K8s default)
    let restart_value = svc
        .deploy
        .as_ref()
        .and_then(|d| d.restart_policy.as_ref())
        .and_then(|rp| rp.condition.as_deref())
        .or(svc.restart.as_deref());
    if let Some(restart) = restart_value {
        let rp = match restart.trim() {
            "no" | "false" | "none" => Some("Never"),
            "on-failure" => Some("OnFailure"),
            _ => None,
        };
        if let Some(rp) = rp {
            s.push_str(&format!("      restartPolicy: {rp}\n"));
        }
    }

    s.push_str("      containers:\n");
    s.push_str(&format!("        - name: {name}\n"));
    s.push_str(&format!("          image: {}\n", svc.image));

    if let Some(cmd) = &svc.command {
        let parts = command_to_array(cmd);
        if !parts.is_empty() {
            s.push_str("          command:\n");
            for p in parts {
                s.push_str(&format!("            - {}\n", yaml_quote(&p)));
            }
        }
    }

    let env_pairs = parse_environment(&svc.environment);
    if !env_pairs.is_empty() {
        s.push_str("          env:\n");
        for (k, v) in env_pairs {
            s.push_str(&format!("            - name: {}\n", k));
            s.push_str(&format!("              value: {}\n", yaml_quote(&v)));
        }
    }

    let ports = parse_ports(&svc.ports);
    if !ports.is_empty() {
        s.push_str("          ports:\n");
        for p in &ports {
            s.push_str(&format!("            - containerPort: {}\n", p.container));
            s.push_str(&format!("              name: tcp-{}\n", p.container));
        }
    }

    if let Some(probe) = render_liveness_probe(svc.healthcheck.as_ref()) {
        s.push_str(&probe);
    }

    if let Some(resources) = render_resources(
        svc.deploy.as_ref().and_then(|d| d.resources.as_ref()),
    ) {
        s.push_str(&resources);
    }

    let parsed = parse_volumes(&svc.volumes);
    let mut lifted_stubs: Vec<ConfigMapStub> = Vec::new();
    let mut lifted_mounts: Vec<(String, String, bool)> = Vec::new(); // (cm_name, mount_path, read_only)
    if opts.lift_bind_mounts {
        for bm in &parsed.bind_mounts {
            let cm_name = bind_mount_configmap_name(name, &bm.source);
            lifted_mounts.push((cm_name.clone(), bm.mount_path.clone(), bm.read_only));
            lifted_stubs.push(ConfigMapStub {
                name: cm_name,
                source: bm.source.clone(),
                mount_path: bm.mount_path.clone(),
                owner_service: name.to_string(),
            });
        }
    }

    // Service-level secrets: map each reference to a Pod volume
    // backed by `secret: { secretName: <id> }` and a mount under
    // `/run/secrets/<target>` (the Compose convention). Long-form
    // entries with `target:` honour their custom path.
    let secret_refs: Vec<MountRef> =
        parse_mount_refs(&svc.secrets, MountRefKind::Secret, &compose.secrets);
    let config_refs: Vec<MountRef> =
        parse_mount_refs(&svc.configs, MountRefKind::Config, &compose.configs);

    let any_volume_mounts = !parsed.named_mounts.is_empty()
        || !lifted_mounts.is_empty()
        || !secret_refs.is_empty()
        || !config_refs.is_empty();
    if any_volume_mounts {
        s.push_str("          volumeMounts:\n");
        for vm in &parsed.named_mounts {
            s.push_str(&format!("            - name: {}\n", vm.volume));
            s.push_str(&format!("              mountPath: {}\n", vm.mount_path));
            if vm.read_only {
                s.push_str("              readOnly: true\n");
            }
        }
        for (cm_name, mount_path, read_only) in &lifted_mounts {
            s.push_str(&format!("            - name: {}\n", cm_name));
            s.push_str(&format!("              mountPath: {}\n", mount_path));
            if *read_only {
                s.push_str("              readOnly: true\n");
            }
        }
        for r in secret_refs.iter().chain(config_refs.iter()) {
            s.push_str(&format!("            - name: {}\n", r.volume_name()));
            s.push_str(&format!(
                "              mountPath: {}\n",
                r.mount_path()
            ));
            // Secrets are read-only by convention; configs follow
            // the same heuristic since Compose never makes them
            // writable either.
            s.push_str("              readOnly: true\n");
        }
    }

    if any_volume_mounts {
        s.push_str("      volumes:\n");
        let mut seen = std::collections::HashSet::new();
        for vm in &parsed.named_mounts {
            if seen.insert(vm.volume.clone()) {
                s.push_str(&format!("        - name: {}\n", vm.volume));
                s.push_str("          persistentVolumeClaim:\n");
                s.push_str(&format!("            claimName: {}\n", vm.volume));
            }
        }
        for (cm_name, _, _) in &lifted_mounts {
            if seen.insert(cm_name.clone()) {
                s.push_str(&format!("        - name: {}\n", cm_name));
                s.push_str("          configMap:\n");
                s.push_str(&format!("            name: {}\n", cm_name));
            }
        }
        for r in &secret_refs {
            if seen.insert(r.volume_name()) {
                s.push_str(&format!("        - name: {}\n", r.volume_name()));
                s.push_str("          secret:\n");
                s.push_str(&format!("            secretName: {}\n", r.id));
            }
        }
        for r in &config_refs {
            if seen.insert(r.volume_name()) {
                s.push_str(&format!("        - name: {}\n", r.volume_name()));
                s.push_str("          configMap:\n");
                s.push_str(&format!("            name: {}\n", r.id));
            }
        }
    }

    if !opts.lift_bind_mounts {
        for warn in &parsed.warnings {
            s.push_str(&format!(
                "      # NOTE: bind mount '{warn}' was dropped — translate to a hostPath\n      #       volume or a ConfigMap before applying.\n"
            ));
        }
    } else if !parsed.bind_mounts.is_empty() {
        s.push_str(
            "      # NOTE: bind mounts were lifted into placeholder ConfigMaps.\n\
             \x20\x20\x20\x20\x20\x20#       Populate them with the real file content before apply,\n\
             \x20\x20\x20\x20\x20\x20#       e.g. `kubectl create configmap NAME --from-file=DIR`.\n",
        );
    }

    if svc.depends_on.is_some() {
        s.push_str(
            "      # NOTE: Compose `depends_on` does not translate to Kubernetes —\n\
             \x20\x20\x20\x20\x20\x20#       use init containers, readiness probes, or a service mesh.\n",
        );
    }

    Ok((s, lifted_stubs))
}

/// Sanitise a Compose source path into a Kubernetes-safe name.
/// `<service>-<source>` lower-cased, with non-DNS-label chars
/// collapsed to `-`. K8s requires names to match `[a-z0-9]([-a-z0-9]*[a-z0-9])?`.
fn bind_mount_configmap_name(service: &str, source: &str) -> String {
    let stripped = source
        .trim_start_matches("./")
        .trim_start_matches('/')
        .trim_end_matches('/');
    let combined = format!("{service}-{stripped}");
    let mut out = String::with_capacity(combined.len());
    let mut last_dash = false;
    for ch in combined.chars() {
        let c = ch.to_ascii_lowercase();
        if c.is_ascii_alphanumeric() {
            out.push(c);
            last_dash = false;
        } else if !last_dash && !out.is_empty() {
            out.push('-');
            last_dash = true;
        }
    }
    while out.ends_with('-') {
        out.pop();
    }
    if out.is_empty() {
        "pierx-cm".to_string()
    } else {
        out
    }
}

/// Translate `deploy.resources` into a K8s `resources:` block on
/// the container. Returns `None` when neither limits nor
/// reservations have any populated field — emitting an empty
/// `resources: {}` block is valid YAML but adds clutter.
///
/// Compose `deploy.resources.limits.{cpus,memory}` →
/// `resources.limits.{cpu,memory}`. `reservations` →
/// `resources.requests`. Field names re-spelled (`cpus` → `cpu`)
/// because Compose and K8s differ on the singular form.
fn render_resources(resources: Option<&ComposeResources>) -> Option<String> {
    let r = resources?;
    let limits_cpu = r.limits.as_ref().and_then(|b| compose_cpu_string(b));
    let limits_mem = r.limits.as_ref().and_then(|b| b.memory.clone());
    let req_cpu = r.reservations.as_ref().and_then(|b| compose_cpu_string(b));
    let req_mem = r.reservations.as_ref().and_then(|b| b.memory.clone());

    if limits_cpu.is_none() && limits_mem.is_none() && req_cpu.is_none() && req_mem.is_none() {
        return None;
    }

    let mut s = String::new();
    s.push_str("          resources:\n");
    let has_limits = limits_cpu.is_some() || limits_mem.is_some();
    let has_requests = req_cpu.is_some() || req_mem.is_some();
    if has_limits {
        s.push_str("            limits:\n");
        if let Some(c) = &limits_cpu {
            s.push_str(&format!("              cpu: {}\n", yaml_quote(c)));
        }
        if let Some(m) = &limits_mem {
            s.push_str(&format!("              memory: {}\n", yaml_quote(m)));
        }
    }
    if has_requests {
        s.push_str("            requests:\n");
        if let Some(c) = &req_cpu {
            s.push_str(&format!("              cpu: {}\n", yaml_quote(c)));
        }
        if let Some(m) = &req_mem {
            s.push_str(&format!("              memory: {}\n", yaml_quote(m)));
        }
    }
    Some(s)
}

/// Pull `cpus:` out of a Compose resource bucket as a string.
/// Compose users sometimes write `cpus: 0.5` (number) and
/// sometimes `cpus: '0.5'` (string); both forms are sane and
/// K8s accepts the string form, so we coerce.
fn compose_cpu_string(bucket: &ComposeResourceBucket) -> Option<String> {
    match bucket.cpus.as_ref()? {
        serde_yml::Value::String(s) => Some(s.clone()),
        serde_yml::Value::Number(n) => Some(n.to_string()),
        _ => None,
    }
}

/// Translate a Compose `healthcheck` into a Kubernetes
/// `livenessProbe:` block. Returns `None` when:
///
///   * no healthcheck was declared, OR
///   * the healthcheck explicitly opted out
///     (`disable: true` / `test: ["NONE"]`), OR
///   * the `test:` shape is unrecognisable — we'd rather skip the
///     probe than emit a manifest that won't apply.
///
/// We only emit `livenessProbe` (not `readinessProbe`). Compose
/// has one healthcheck per service that maps cleanly onto
/// liveness semantics ("kill if persistently unhealthy"); a
/// readiness probe would double the per-pod probe traffic for a
/// guess about the user's intent.
fn render_liveness_probe(hc: Option<&ComposeHealthcheck>) -> Option<String> {
    let hc = hc?;
    if hc.disable.unwrap_or(false) {
        return None;
    }
    let argv = parse_healthcheck_test(hc.test.as_ref())?;
    if argv.is_empty() {
        return None;
    }

    let mut s = String::new();
    s.push_str("          livenessProbe:\n");
    s.push_str("            exec:\n");
    s.push_str("              command:\n");
    for token in &argv {
        s.push_str(&format!("                - {}\n", yaml_quote(token)));
    }
    if let Some(secs) = hc.interval.as_deref().and_then(parse_compose_duration_secs) {
        s.push_str(&format!("            periodSeconds: {secs}\n"));
    }
    if let Some(secs) = hc.timeout.as_deref().and_then(parse_compose_duration_secs) {
        s.push_str(&format!("            timeoutSeconds: {secs}\n"));
    }
    if let Some(n) = hc.retries {
        s.push_str(&format!("            failureThreshold: {n}\n"));
    }
    if let Some(secs) = hc
        .start_period
        .as_deref()
        .and_then(parse_compose_duration_secs)
    {
        s.push_str(&format!("            initialDelaySeconds: {secs}\n"));
    }
    Some(s)
}

/// Parse Compose's `healthcheck.test` field into an argv vector
/// ready for K8s `exec.command`. Returns `None` for the explicit
/// "no healthcheck" sentinel (`["NONE"]`) and for shapes we don't
/// recognise. Otherwise:
///
///  * `"curl foo"` → `["sh", "-c", "curl foo"]` (CMD-SHELL fallback)
///  * `["CMD", "curl", "foo"]` → `["curl", "foo"]`
///  * `["CMD-SHELL", "curl foo"]` → `["sh", "-c", "curl foo"]`
///  * `["NONE"]` → `None`
fn parse_healthcheck_test(value: Option<&serde_yml::Value>) -> Option<Vec<String>> {
    let v = value?;
    match v {
        serde_yml::Value::String(s) => {
            // Bare-string form is implicitly CMD-SHELL.
            let trimmed = s.trim();
            if trimmed.is_empty() {
                return None;
            }
            Some(vec!["sh".to_string(), "-c".to_string(), trimmed.to_string()])
        }
        serde_yml::Value::Sequence(items) => {
            let strs: Vec<String> = items
                .iter()
                .filter_map(|i| i.as_str().map(|s| s.to_string()))
                .collect();
            if strs.is_empty() {
                return None;
            }
            match strs[0].as_str() {
                "NONE" => None,
                "CMD" => Some(strs.into_iter().skip(1).collect()),
                "CMD-SHELL" => {
                    // Spec says exactly one shell-string follows.
                    let script = strs.into_iter().nth(1).unwrap_or_default();
                    if script.is_empty() {
                        None
                    } else {
                        Some(vec!["sh".to_string(), "-c".to_string(), script])
                    }
                }
                // No `CMD` / `CMD-SHELL` prefix → treat the whole
                // array as the argv (defensive — some users write
                // shorter forms).
                _ => Some(strs),
            }
        }
        _ => None,
    }
}

/// Parse a Compose duration like `"30s"`, `"1m30s"`, `"2h"` into
/// seconds, rounded down. Returns `None` for unparseable input
/// (we'd rather omit the field than emit nonsense). Sub-second
/// suffixes (`ms`, `us`, `ns`) are clamped to a 1-second floor —
/// K8s probe knobs are integer-second.
fn parse_compose_duration_secs(s: &str) -> Option<u64> {
    let t = s.trim();
    if t.is_empty() {
        return None;
    }
    // Bare integer = seconds (Compose accepts plain numbers as ns
    // strictly, but in practice users write `30` meaning seconds.
    // We bias towards user-friendly behaviour.)
    if let Ok(n) = t.parse::<u64>() {
        return Some(n);
    }

    // Walk char-by-char accumulating a (number, unit) pair stream.
    let mut total: u64 = 0;
    let mut acc: u64 = 0;
    let mut got_digit = false;
    let mut chars = t.chars().peekable();
    while let Some(&c) = chars.peek() {
        if c.is_ascii_digit() {
            acc = acc.saturating_mul(10).saturating_add((c as u64) - 48);
            got_digit = true;
            chars.next();
            continue;
        }
        // Read a unit (1 or 2 letter).
        let mut unit = String::new();
        while let Some(&u) = chars.peek() {
            if u.is_ascii_alphabetic() {
                unit.push(u);
                chars.next();
            } else {
                break;
            }
        }
        let mult: u64 = match unit.as_str() {
            "h" => 3600,
            "m" => 60,
            "s" => 1,
            // Sub-second units floor to 1s — probes don't go
            // finer than seconds at the K8s API level.
            "ms" | "us" | "µs" | "ns" => 0, // accumulate as zero, will floor below
            _ => return None,
        };
        if mult == 0 && acc > 0 {
            // Sub-second component — bump total by 1 if it's the
            // ONLY component, otherwise just discard it.
            if total == 0 && chars.peek().is_none() {
                return Some(1);
            }
        } else {
            total = total.saturating_add(acc.saturating_mul(mult));
        }
        acc = 0;
    }
    if !got_digit {
        return None;
    }
    if total == 0 {
        // Pure-number input was handled above; reaching here means
        // the input was something like "0s" — keep as 0.
        Some(0)
    } else {
        Some(total)
    }
}

/// One service-level secret/config reference resolved to a Pod
/// volume + volumeMount. We carry the mount kind so the volume
/// emit code knows whether to write `secret:` or `configMap:`.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
enum MountRefKind {
    Secret,
    Config,
}

#[derive(Debug, Clone)]
struct MountRef {
    /// Top-level key — used as both the K8s resource name and
    /// the in-Pod volume name (sanitised on the way to the
    /// volume name).
    id: String,
    /// Mount path inside the container. Defaults to
    /// `/run/secrets/<id>` for secrets and `/<id>` for configs
    /// when the long-form `target:` is absent.
    target: String,
    kind: MountRefKind,
}

impl MountRef {
    fn volume_name(&self) -> String {
        // Same DNS-label sanitiser as bind mounts — `secret-foo`
        // / `config-foo` prefix avoids collisions when a secret
        // and config share a name.
        let prefix = match self.kind {
            MountRefKind::Secret => "secret",
            MountRefKind::Config => "config",
        };
        bind_mount_configmap_name(prefix, &self.id)
    }
    fn mount_path(&self) -> &str {
        &self.target
    }
}

fn parse_mount_refs(
    items: &[serde_yml::Value],
    kind: MountRefKind,
    top_level: &BTreeMap<String, serde_yml::Value>,
) -> Vec<MountRef> {
    let mut out = Vec::new();
    for v in items {
        match v {
            serde_yml::Value::String(name) => {
                if !top_level.contains_key(name) {
                    // Reference to an undeclared secret/config —
                    // skip silently; emit-time validation in
                    // kubectl will tell the user.
                    continue;
                }
                out.push(MountRef {
                    id: name.clone(),
                    target: default_target(name, kind),
                    kind,
                });
            }
            serde_yml::Value::Mapping(m) => {
                let source = m
                    .get(serde_yml::Value::String("source".into()))
                    .and_then(|v| v.as_str())
                    .map(|s| s.to_string());
                let target = m
                    .get(serde_yml::Value::String("target".into()))
                    .and_then(|v| v.as_str())
                    .map(|s| s.to_string());
                let Some(id) = source else { continue };
                if !top_level.contains_key(&id) {
                    continue;
                }
                let target = target.unwrap_or_else(|| default_target(&id, kind));
                out.push(MountRef {
                    id,
                    target,
                    kind,
                });
            }
            _ => {}
        }
    }
    out
}

fn default_target(id: &str, kind: MountRefKind) -> String {
    match kind {
        // Compose's documented default for service-level secrets.
        MountRefKind::Secret => format!("/run/secrets/{id}"),
        // Compose mounts top-level configs at `/<id>` by default.
        MountRefKind::Config => format!("/{id}"),
    }
}

/// Emit a `Secret` placeholder for a top-level Compose `secrets:`
/// declaration. We honour the `file:` form by referencing the
/// expected path in a placeholder comment; `external:` and
/// `environment:` forms get a NOTE because both require cluster-
/// side wiring before apply.
fn render_secret_stub(
    name: &str,
    body: &serde_yml::Value,
    namespace: Option<&str>,
) -> String {
    let ns_line = match namespace {
        Some(n) => format!("  namespace: {n}\n"),
        None => String::new(),
    };
    let (note, hint) = mount_stub_hints(body);
    let mut s = String::new();
    s.push_str("apiVersion: v1\n");
    s.push_str("kind: Secret\n");
    s.push_str("metadata:\n");
    s.push_str(&ns_line);
    s.push_str(&format!("  name: {name}\n"));
    s.push_str("type: Opaque\n");
    s.push_str("stringData:\n");
    s.push_str("  README.md: |\n");
    s.push_str("    Placeholder Secret generated by Pier-X.\n");
    if let Some(h) = hint {
        s.push_str(&format!("    {h}\n"));
    }
    s.push_str(
        "    Replace this entry with the real key/value pairs, e.g.:\n\
         \x20\x20\x20\x20\x20\x20kubectl create secret generic ",
    );
    s.push_str(name);
    s.push_str(" --from-literal=KEY=value --dry-run=client -o yaml | kubectl apply -f -\n");
    if let Some(n) = note {
        s.push_str(&format!("# NOTE: {n}\n"));
    }
    s
}

