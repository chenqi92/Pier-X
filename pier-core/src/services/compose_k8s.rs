/// Inspect a Compose `secrets:` / `configs:` entry body for
/// `file:` / `external:` / `environment:` shapes. Returns
/// `(top-level NOTE, in-data hint)`. Either may be `None` when
/// the entry is empty / unknown.
fn mount_stub_hints(body: &serde_yml::Value) -> (Option<String>, Option<String>) {
    let serde_yml::Value::Mapping(m) = body else {
        return (None, None);
    };
    if let Some(file) = m
        .get(serde_yml::Value::String("file".into()))
        .and_then(|v| v.as_str())
    {
        return (
            None,
            Some(format!("Compose source file: {file}")),
        );
    }
    if m.get(serde_yml::Value::String("external".into()))
        .and_then(|v| v.as_bool())
        .unwrap_or(false)
    {
        return (
            Some(
                "Compose `external: true` — reference an existing cluster resource"
                    .to_string(),
            ),
            Some(
                "External resource — make sure it already exists in the cluster.".to_string(),
            ),
        );
    }
    if let Some(env) = m
        .get(serde_yml::Value::String("environment".into()))
        .and_then(|v| v.as_str())
    {
        return (
            Some(format!(
                "Compose `environment: {env}` — sourced from env at compose-up time"
            )),
            Some(format!("Source env var on the build host: {env}")),
        );
    }
    (None, None)
}

/// Pull the membership list out of a service's `networks:`
/// field. Compose accepts both a sequence of names and a mapping
/// keyed by name (with optional aliases / ipv4_address / etc. as
/// the value); we only want the names. Filtered to networks that
/// are also declared at the top level — referencing a network
/// that doesn't exist is a Compose-side error and we don't want
/// to mint K8s labels for it.
fn parse_service_networks(
    value: &Option<serde_yml::Value>,
    top_level: &BTreeMap<String, serde_yml::Value>,
) -> Vec<String> {
    let Some(v) = value else { return Vec::new() };
    let mut names: Vec<String> = match v {
        serde_yml::Value::Sequence(seq) => seq
            .iter()
            .filter_map(|i| i.as_str().map(|s| s.to_string()))
            .collect(),
        serde_yml::Value::Mapping(m) => m
            .iter()
            .filter_map(|(k, _)| k.as_str().map(|s| s.to_string()))
            .collect(),
        _ => Vec::new(),
    };
    names.retain(|n| top_level.contains_key(n));
    // Stable order: alphabetical so two services on the same set
    // of networks render their labels in the same order.
    names.sort();
    names.dedup();
    names
}

/// Emit a placeholder `NetworkPolicy` for a top-level
/// `networks:` entry. The policy:
///
///   * selects pods labelled `pier-x.network/<name>: "in"`,
///   * declares Ingress only,
///   * allows ingress from any pod with the same label.
///
/// This is the closest shape to "Compose network = pods can talk
/// to each other" that NetworkPolicy can express. The trailing
/// NOTE warns the user that NetworkPolicy is namespace-scoped
/// default-allow unless their CNI says otherwise — applying this
/// alone won't isolate the pod from outside traffic.
fn render_network_policy(name: &str, namespace: Option<&str>) -> String {
    let ns_line = match namespace {
        Some(n) => format!("  namespace: {n}\n"),
        None => String::new(),
    };
    format!(
        "apiVersion: networking.k8s.io/v1\n\
         kind: NetworkPolicy\n\
         metadata:\n\
         {ns}\
         \x20\x20name: pier-x-net-{name}\n\
         spec:\n\
         \x20\x20podSelector:\n\
         \x20\x20\x20\x20matchLabels:\n\
         \x20\x20\x20\x20\x20\x20pier-x.network/{name}: \"in\"\n\
         \x20\x20policyTypes:\n\
         \x20\x20\x20\x20- Ingress\n\
         \x20\x20ingress:\n\
         \x20\x20\x20\x20- from:\n\
         \x20\x20\x20\x20\x20\x20\x20\x20- podSelector:\n\
         \x20\x20\x20\x20\x20\x20\x20\x20\x20\x20\x20\x20matchLabels:\n\
         \x20\x20\x20\x20\x20\x20\x20\x20\x20\x20\x20\x20\x20\x20pier-x.network/{name}: \"in\"\n\
         # NOTE: NetworkPolicy is enforced by the CNI plugin (Calico,\n\
         #       Cilium, etc.). Without one configured, this policy is\n\
         #       a no-op. Add a default-deny policy for the namespace\n\
         #       if you actually want isolation.\n",
        ns = ns_line,
        name = name,
    )
}

