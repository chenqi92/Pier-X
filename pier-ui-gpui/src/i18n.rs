// Pier-X GPUI spike — interface localization (English / 简体中文).
//
// A process-global current language plus a free `t(key)` lookup, so the
// translation works from anywhere — including `const` string tables (TOOLS,
// PAGES, KEYMAP) and `&self` widget helpers that have no `cx` to reach a GPUI
// global. Switching language flips the atomic and calls `window.refresh()`,
// the same shape as the theme toggle; every view re-reads `t()` on its next
// paint.
//
// The catalog is split: `en()` holds the full key→string table; `zh()` only
// overrides the keys whose Chinese differs from the English. Brand / protocol
// names (GIT, DOCKER, MYSQL, POSTGRES, REDIS, SQLITE, SFTP, MARKDOWN) get no
// `zh` entry and therefore stay in English. Lookups fall back zh → en → key,
// so a missing key paints its own name (a visible reminder to add it).

use std::sync::atomic::{AtomicU8, Ordering};

use gpui::SharedString;

/// Interface language.
#[derive(Clone, Copy, PartialEq, Eq, Debug)]
pub enum Lang {
    En,
    Zh,
}

impl Lang {
    /// Stable code used for persistence (`UiState.lang`).
    pub fn code(self) -> &'static str {
        match self {
            Lang::En => "en",
            Lang::Zh => "zh",
        }
    }

    /// Parse a persisted code; anything unknown falls back to English.
    pub fn from_code(s: &str) -> Lang {
        match s {
            "zh" => Lang::Zh,
            _ => Lang::En,
        }
    }

    /// Native display name, shown in the language switcher.
    pub fn label(self) -> &'static str {
        match self {
            Lang::En => "English",
            Lang::Zh => "中文",
        }
    }

    /// The two selectable languages, in switcher order.
    pub fn all() -> [Lang; 2] {
        [Lang::En, Lang::Zh]
    }
}

// 0 = English, 1 = 中文. Plain atomic rather than a gpui Global so `t()` needs
// no `cx`; the UI thread is the only writer (the switcher) and reader (paint).
static CURRENT: AtomicU8 = AtomicU8::new(0);

/// The active interface language.
pub fn current() -> Lang {
    match CURRENT.load(Ordering::Relaxed) {
        1 => Lang::Zh,
        _ => Lang::En,
    }
}

/// Set the active interface language. Caller is responsible for repainting
/// (the shell calls `window.refresh()` after this).
pub fn set(lang: Lang) {
    CURRENT.store(if lang == Lang::Zh { 1 } else { 0 }, Ordering::Relaxed);
}

/// Translate `key` into the current language. Hits return a static string with
/// no allocation; a miss returns the key itself so it's obvious on screen.
pub fn t(key: &str) -> SharedString {
    let hit = match current() {
        Lang::En => en(key),
        Lang::Zh => zh(key).or_else(|| en(key)),
    };
    match hit {
        Some(s) => SharedString::from(s),
        None => SharedString::from(key.to_owned()),
    }
}

/// Translate a template and fill `{0}`, `{1}`, … placeholders in order.
/// Used for the handful of strings that interpolate counts / values.
pub fn tf(key: &str, args: &[&str]) -> String {
    let mut s = t(key).to_string();
    for (i, a) in args.iter().enumerate() {
        s = s.replace(&format!("{{{i}}}"), a);
    }
    s
}

